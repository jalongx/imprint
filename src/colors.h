#ifndef COLORS_H
#define COLORS_H

/* Foreground colors */
#define RESET   "\033[0m"
#define BLACK   "\033[30m"
#define RED     "\033[31m"
#define GREEN   "\033[32m"
#define YELLOW  "\033[38;2;255;240;0m"
#define BLUE    "\033[34m"
#define MAGENTA "\033[35m"
#define CYAN    "\033[36m"
#define WHITE   "\033[37m"

/* Bold variants (optional) */
#define BOLD    "\033[1m"
#define DIM     "\033[2m"

#endif
